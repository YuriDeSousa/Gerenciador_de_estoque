    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        Document doc = new Document();  
        Calendar c = Calendar.getInstance();
        String dia = c.get(Calendar.DAY_OF_MONTH) + "";
        String mes = c.get(Calendar.MONTH ) + 1 + "";
        String ano = c.get(Calendar.YEAR) + "";
        ProdutoDAO pdao = new ProdutoDAO();
        String diretorio = System.getProperty("user.dir");
    	String dirRelatorio = diretorio+"/Arquivos/Relatorio";
        try {
        	
            PdfWriter.getInstance(doc, new FileOutputStream(dirRelatorio));
            doc.open();
            
            Paragraph date = new Paragraph(dia + "/" + mes + "/" + ano);
            date.setAlignment(2);
            doc.add(date);
            doc.add(new Paragraph("  "));
            float[] columnWidths = {1,4,1,2,2};
            date.setAlignment(2);
            PdfPTable table = new PdfPTable(columnWidths);
            table.setWidthPercentage(100);
            PdfPCell cel = new PdfPCell(new Paragraph("COD"));
            PdfPCell cel1 = new PdfPCell(new Paragraph("NOME"));
            PdfPCell cel2 = new PdfPCell(new Paragraph("QTD"));
            PdfPCell cel3 = new PdfPCell(new Paragraph("PREÇO UNT"));
            PdfPCell cel4 = new PdfPCell(new Paragraph("PREÇO BRUTO"));
            table.addCell(cel);

            table.addCell(cel1);
            table.addCell(cel2);
            table.addCell(cel3);
            table.addCell(cel4);
            Font FProdutos = new Font(Font.FontFamily.HELVETICA, 9);
            for(Produto p: pdao.read()){
                cel = new PdfPCell(new Paragraph(p.getCod() + "", FProdutos));
                cel1 = new PdfPCell(new Paragraph(p.getNome(), FProdutos));
                cel2 = new PdfPCell(new Paragraph((int) p.getQtd()+"", FProdutos));
                String cl3 = ""+ NumberFormat.getCurrencyInstance().format((float) p.getValor());

                cel3 = new PdfPCell(new Paragraph(cl3, FProdutos));
                String  cl4 = "" + NumberFormat.getCurrencyInstance().format(p.getQtd() *  p.getValor());

                cel4 = new PdfPCell(new Paragraph(cl4, FProdutos));
                table.addCell(cel);
                table.addCell(cel1);
                table.addCell(cel2);
                table.addCell(cel3);
                table.addCell(cel4);

            }
            doc.add(table);
            double total_produtos = 0;
            for(Produto p: pdao.read()){
                total_produtos += (p.getQtd() *  p.getValor());
            }
            Paragraph total = new Paragraph("TOTAL: " + NumberFormat.getCurrencyInstance().format(total_produtos));
            total.setAlignment(2);
            doc.add(total);


           } catch (FileNotFoundException | DocumentException ex) {
               JOptionPane.showMessageDialog(null, "Erro: " + ex, "[ERRO]", JOptionPane.ERROR_MESSAGE);
           } catch (IOException ex) {
            Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
        }finally{
               doc.close();
           }
            try {
                Desktop.getDesktop().open(new File(dirRelatorio));
                JOptionPane.showMessageDialog(null, "Relatório concluído");

            } catch (IOException ex) {
                JOptionPane.showMessageDialog(null, "Erro: " + ex, "[ERRO]", JOptionPane.ERROR_MESSAGE);
            }
        
        
        
    }//GEN-LAST:event_jButton8ActionPerformed

