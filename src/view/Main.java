    public void searchForNome(String nome){
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        ProdutoDAO pdao = new ProdutoDAO();
        modelo.setNumRows(0);
        for (Produto p: pdao.search(nome)) {
            modelo.addRow(new Object[]{
                p.getCod(),
                p.getNome(),
                p.getQtd(),
                p.getValor()
            
            });
        }
    }
    public void notificacaoContagem(){
        ProdutoDAO pdao = new ProdutoDAO();
        int not = 0;
        for(Produto prod : pdao.read()){
            if(prod.getQtd() <= 5){
                not++;
                
            }
            
            
        }
        if(not > 0){
            jButton2.setForeground(new java.awt.Color(255, 0, 0));
            jButton2.setText("Notificações(" + not + ")");
        }else{
            jButton2.setForeground(new java.awt.Color(0, 0, 0));
            jButton2.setText("Notificações");
        }
        
        
    }
