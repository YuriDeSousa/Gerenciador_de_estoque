    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        try{

            if(nome_ad.getText().trim().equals("") ){
                JOptionPane.showMessageDialog(null, "Preencha todos os campos corretamente!", "[ERRO]", JOptionPane.ERROR_MESSAGE);
            }else{

                ProdutoDAO dao = new ProdutoDAO();
                Produto p = new Produto(nome_ad.getText().trim(), Double.parseDouble(valor_ad.getText().trim()), Integer.parseInt(qtd_ad.getText().trim()) );
                dao.create(p);
                nome_ad.setText("");
                qtd_ad.setText("");
                valor_ad.setText("");

                readJTable();
                notificacaoContagem();
            }
        }catch(HeadlessException | NumberFormatException ex){
            JOptionPane.showMessageDialog(null, "Preencha todos os campos corretamente!", "[ERRO]", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

