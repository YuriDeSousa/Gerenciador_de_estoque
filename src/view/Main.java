    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        if(jTable1.getSelectedRow() != -1){
            Object[] options = { "Sim", "Não" };
            int crtz = JOptionPane.showOptionDialog(null, "Tem certeza?", "Excluir",
            JOptionPane.DEFAULT_OPTION, JOptionPane.WARNING_MESSAGE,
            null, options, options[0]);
            if(crtz == 0){
                Produto p = new Produto();
                ProdutoDAO dao = new ProdutoDAO();
                p.setCod((int) jTable1.getValueAt(jTable1.getSelectedRow(), 0));
                dao.delete(p);
                readJTable();
                notificacaoContagem();
            }
           
            
        }else{
            JOptionPane.showMessageDialog(null, "Selecione um produto para excluir!", "[ERRO]", JOptionPane.ERROR_MESSAGE );
        }
        
        
        
    }//GEN-LAST:event_jButton5ActionPerformed

