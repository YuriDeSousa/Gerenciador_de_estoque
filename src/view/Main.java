    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        
        try{
          if(jTable1.getSelectedRow() != -1){
            Produto p = new Produto();
            

            int qtd = Integer.parseInt(JOptionPane.showInputDialog(null, "Quantidade:"));
            
            p.setNome(jTable1.getValueAt(jTable1.getSelectedRow(), 1).toString());
            

            p.setCod((int) jTable1.getValueAt(jTable1.getSelectedRow(), 0));
            p.setQtd((int) jTable1.getValueAt(jTable1.getSelectedRow(), 2));

            p.setValor((double)jTable1.getValueAt(jTable1.getSelectedRow(), 3));
            p.setQtd_venda(qtd);
            if(p.getQtd() < p.getQtd_venda() || p.getQtd_venda() <= 0){
                JOptionPane.showMessageDialog(null, "O número informado na venda é negativo, 0 ou maior que o estoque!", "[ERRO]", JOptionPane.ERROR_MESSAGE);
            }else{
                aP.add(p);
                JOptionPane.showMessageDialog(null, "Produto adicionado ao carrinho!");
            }
            
          }else{
                JOptionPane.showMessageDialog(null, "Selecione um produto para adicionar!", "[ERRO]", JOptionPane.ERROR_MESSAGE);
          }  
        }catch(NumberFormatException ex){
            JOptionPane.showMessageDialog(null, "A operação foi cancelada", "Aviso", JOptionPane.WARNING_MESSAGE);
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, "Erro: "+ ex, "[ERRO]", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_jButton7ActionPerformed

