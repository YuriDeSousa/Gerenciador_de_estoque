    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
            if(tela.jTable1.getValueAt(tela.jTable1.getSelectedRow(), 1).toString().equals(nome_att.getText()) && tela.jTable1.getValueAt(tela.jTable1.getSelectedRow(), 2).toString().equals(qtd_att.getText()) && tela.jTable1.getValueAt(tela.jTable1.getSelectedRow(), 3).toString().equals(valor_att.getText())){
                JOptionPane.showMessageDialog(null, "Não houve nenhuma alteração!", "[ERRO]", JOptionPane.ERROR_MESSAGE);
            }else{
                if(nome_att.getText().trim().equals("")){
                    JOptionPane.showMessageDialog(null, "Preencha todos os campos corretamente!", "[ERRO]", JOptionPane.ERROR_MESSAGE);
                }else{
                    ProdutoDAO dao = new ProdutoDAO();
                    Produto p = new Produto(nome_att.getText().trim(), Double.parseDouble(valor_att.getText().trim()), Integer.parseInt(qtd_att.getText().trim()) );
                    p.setCod((int) tela.jTable1.getValueAt(tela.jTable1.getSelectedRow(), 0));
                    dao.update(p);       
                    nome_att.setText("");
                    valor_att.setText("");
                    qtd_att.setText("");      
                    tela.readJTable();
                    tela.notificacaoContagem();
                    this.dispose();
                }
            }
            
            
            
        }catch(HeadlessException | NumberFormatException ex){
            JOptionPane.showMessageDialog(null, "Preencha todos os campos corretamente!", "[ERRO]", JOptionPane.ERROR_MESSAGE);
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

